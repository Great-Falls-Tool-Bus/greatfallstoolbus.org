// `just source-map-build` — derive the page source map from the routes tree.
//
// Walks src/routes/**/+page.svelte and emits src/lib/generated/source-map.json,
// mapping each SvelteKit route id to its repo-relative source path plus the
// repo URL and default branch. SourceLink.svelte reads this file to render the
// "View source" / "Edit this page" affordance in the shared PageHeader.
//
// Why derived, not per-page hand-links: the map is generated from architectural
// zero so a new page cannot silently lack the affordance, and NO org/repo string
// is hardcoded in any component (it flows from tinyland.repo.json / package.json,
// so every scaffold spoke gets a correct map for free).
//
// Output is deterministic (sorted route keys, tab-indented, trailing newline).
// `just source-map-check` runs this then `git diff --exit-code`, so drift fails
// CI. Run with `pnpm exec tsx` (or node — this file is dependency-free).
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = fileURLToPath(new URL('..', import.meta.url));
const ROUTES_DIR = path.join(ROOT, 'src', 'routes');
const OUT_FILE = path.join(ROOT, 'src', 'lib', 'generated', 'source-map.json');

// Portable across every scaffold spoke: derive the GitHub org/repo from config,
// never from a literal in the generator or a component. Default branch is the
// house convention (main) and can be overridden per-repo in tinyland.repo.json.
const DEFAULT_BRANCH = 'main';

async function readJson(file) {
	try {
		return JSON.parse(await fs.readFile(file, 'utf8'));
	} catch {
		return null;
	}
}

async function resolveRepo() {
	const manifest = await readJson(path.join(ROOT, 'tinyland.repo.json'));
	const pkg = await readJson(path.join(ROOT, 'package.json'));

	// 1) tinyland.repo.json repo.github ("org/repo") — the canonical source.
	// 2) package.json repository.url — the portable fallback.
	let repoUrl = '';
	const github = manifest?.repo?.github;
	if (typeof github === 'string' && github.includes('/')) {
		repoUrl = `https://github.com/${github}`;
	} else {
		const url = typeof pkg?.repository === 'string' ? pkg.repository : pkg?.repository?.url;
		if (typeof url === 'string' && url) {
			repoUrl = url
				.replace(/^git\+/, '')
				.replace(/\.git$/, '')
				.replace(/^git@github\.com:/, 'https://github.com/');
		}
	}
	if (!repoUrl) {
		throw new Error('source-map-build: could not resolve repo URL from tinyland.repo.json or package.json');
	}

	const branch = (typeof manifest?.repo?.defaultBranch === 'string' && manifest.repo.defaultBranch) || DEFAULT_BRANCH;

	return { repoUrl, branch };
}

// SvelteKit route id from a +page.svelte path, base-stripped ("/" for root).
// Drops route-group segments like "(marketing)" the same way SvelteKit does.
function routeIdFor(relPageFile) {
	const dir = path.dirname(relPageFile).slice('src/routes'.length);
	const segments = dir.split('/').filter((s) => s && !(s.startsWith('(') && s.endsWith(')')));
	return segments.length ? `/${segments.join('/')}` : '/';
}

async function walkPageFiles(dir) {
	const out = [];
	for (const entry of await fs.readdir(dir, { withFileTypes: true })) {
		const full = path.join(dir, entry.name);
		if (entry.isDirectory()) {
			out.push(...(await walkPageFiles(full)));
		} else if (entry.name === '+page.svelte') {
			out.push(full);
		}
	}
	return out;
}

async function main() {
	const { repoUrl, branch } = await resolveRepo();
	const pageFiles = await walkPageFiles(ROUTES_DIR);

	const routes = {};
	for (const full of pageFiles) {
		const rel = path.relative(ROOT, full).split(path.sep).join('/');
		routes[routeIdFor(rel)] = rel;
	}

	// Deterministic: sort route keys so the generated file is stable in git.
	const sortedRoutes = {};
	for (const key of Object.keys(routes).sort()) {
		sortedRoutes[key] = routes[key];
	}

	const doc = {
		$generated: 'generated by just source-map-build; do not edit by hand',
		repoUrl,
		branch,
		routes: sortedRoutes,
	};

	const content = `${JSON.stringify(doc, null, '\t')}\n`;
	await fs.mkdir(path.dirname(OUT_FILE), { recursive: true });
	await fs.writeFile(OUT_FILE, content);

	console.log(
		`source-map-build: mapped ${Object.keys(sortedRoutes).length} route(s) -> src/lib/generated/source-map.json`,
	);
}

main().catch((err) => {
	console.error(err);
	process.exit(1);
});
